n1=int(input("Ingrese primer numero"))
n2=int(input("Ingrese segundo numero"))
if n1>n2:
    suma=n1+n2
    diferencia=n1-n2
    print("La suma de los dos numeros es:")
    print(suma)
    print("La diferencia de los dos numeros es:")
    print(diferencia)
else:
    producto=n1*n2
    division=n1/n2
    print("El producto de los dos numeros es:")
    print(producto)
    print("La division de los dos numeros es:")
    print(division)
